     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $post = new Post;
        $post->fullname = $request->input('Name');
        $post->gender = $request->input('Gender');
        $post->dob = $request->input('dob');
        $post->nic = $request->input('Nic');
        $post->address1 = $request->input('Address1');
        $post->address2 = $request->input('Address2');
        $post->city = $request->input('City');
        $post->phone = $request->input('phone-number');
        $post->email = $request->input('Email');
        $post->username = $request->input('Username');
        $post->password = $request->input('Password');

        $post->save();

        // return redirect('/posts');
        return redirect('/register')->with('success','successfully registered');

    }

